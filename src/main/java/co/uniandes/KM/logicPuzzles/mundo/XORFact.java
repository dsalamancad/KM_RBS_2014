package co.uniandes.KM.logicPuzzles.mundo;

public class XORFact {

	private Fact fact1, fact2, fact3, fact4;

	public XORFact(Fact fact1, Fact fact2, Fact fact3, Fact fact4) {
		super();
		this.fact1 = fact1;
		this.fact2 = fact2;
		this.fact3 = fact3;
		this.fact4 = fact4;
	}
	/**
	 * @return the fact1
	 */
	public Fact getFact1() {
		return fact1;
	}
	/**
	 * @param fact1 the fact1 to set
	 */
	public void setFact1(Fact fact1) {
		this.fact1 = fact1;
	}
	/**
	 * @return the fact2
	 */
	public Fact getFact2() {
		return fact2;
	}
	/**
	 * @param fact2 the fact2 to set
	 */
	public void setFact2(Fact fact2) {
		this.fact2 = fact2;
	}
	/**
	 * @return the fact3
	 */
	public Fact getFact3() {
		return fact3;
	}
	/**
	 * @param fact3 the fact3 to set
	 */
	public void setFact3(Fact fact3) {
		this.fact3 = fact3;
	}
	/**
	 * @return the fact4
	 */
	public Fact getFact4() {
		return fact4;
	}
	/**
	 * @param fact4 the fact4 to set
	 */
	public void setFact4(Fact fact4) {
		this.fact4 = fact4;
	}

}
